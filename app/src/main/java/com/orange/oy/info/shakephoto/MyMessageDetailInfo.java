public class MyMessageDetailInfo {

    /**
     * project_name : 项目名称
     * begin_date : 开始日期
     * end_date : 结束日期
     * share_username : 分享人昵称
     * outlet_id : 网点id
     * outlet_name : 网点名称
     * user_name : 用户昵称
     * user_mobile : 用户账号
     * complete_time : 完成时间
     * create_time : 创建时间
     */

    private String project_name;
    private String begin_date;
    private String end_date;
    private String share_username;
    private String outlet_id;
    private String outlet_name;
    private String user_name;
    private String user_mobile;
    private String complete_time;
    private String create_time;

    public String getProject_name() {
        return project_name;
    }

    public void setProject_name(String project_name) {
        this.project_name = project_name;
    }

    public String getBegin_date() {
        return begin_date;
    }

    public void setBegin_date(String begin_date) {
        this.begin_date = begin_date;
    }

    public String getEnd_date() {
        return end_date;
    }

    public void setEnd_date(String end_date) {
        this.end_date = end_date;
    }

    public String getShare_username() {
        return share_username;
    }

    public void setShare_username(String share_username) {
        this.share_username = share_username;
    }

    public String getOutlet_id() {
        return outlet_id;
    }

    public void setOutlet_id(String outlet_id) {
        this.outlet_id = outlet_id;
    }

    public String getOutlet_name() {
        return outlet_name;
    }

    public void setOutlet_name(String outlet_name) {
        this.outlet_name = outlet_name;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getUser_mobile() {
        return user_mobile;
    }

    public void setUser_mobile(String user_mobile) {
        this.user_mobile = user_mobile;
    }

    public String getComplete_time() {
        return complete_time;
    }

    public void setComplete_time(String complete_time) {
        this.complete_time = complete_time;
    }

    public String getCreate_time() {
        return create_time;
    }

    public void setCreate_time(String create_time) {
        this.create_time = create_time;
    }
}
